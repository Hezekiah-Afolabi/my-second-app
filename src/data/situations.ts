export function getSituationById(id: string): Situation | undefined {
  return SITUATIONS.find(s => s.id === id);
}

export function getSituationsByCategory(categoryId: string): Situation[] {
  return SITUATIONS.filter(s => s.category === categoryId);
}

export function searchSituations(query: string): Situation[] {
  const q = query.toLowerCase();
  return SITUATIONS.filter(s =>
    s.title.toLowerCase().includes(q) ||
    s.books.some(b =>
      b.title.toLowerCase().includes(q) ||
      b.author.toLowerCase().includes(q) ||
      b.description.toLowerCase().includes(q)
    )
  );
}
